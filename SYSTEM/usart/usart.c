//CopeSerial3Data为串口3中断调用函数，串口每接收到一个数据，调用一次这个函数
void CopeSerial3Data(unsigned char ucData)
{
	static unsigned char ucRxBuffer[250];
	static unsigned char ucRxCnt = 0;
	ucRxBuffer[ucRxCnt++]=ucData;
	if(ucRxBuffer[0]!=0x55)
	{
		ucRxCnt=0;
		return;
	}
	if(ucRxCnt<11)
	{
		return;
	}
	else
	{
		switch(ucRxBuffer[1])
		{
			case 0x51: 
				memcpy(&sAcctemp,&ucRxBuffer[2],6);
				//sAcctemp.time=now;
				stcAcc[accSize]=sAcctemp;
				accSize=accSize>=buffersize-1?0:accSize+1;
				break;
			case 0x52: 
				memcpy(&sGyrotemp,&ucRxBuffer[2],6);
				//sGyrotemp.time=now;
				stcGyro[gyroSize]=sGyrotemp;
				gyroSize=gyroSize>=buffersize-1?0:gyroSize+1;
				break;
			case 0x53: 
				memcpy(&sAngletemp,&ucRxBuffer[2],6);
				//sAngletemp.time=now;
				stcAngle[angleSize]=sAngletemp;
				angleSize=angleSize>=buffersize-1?0:angleSize+1;
				break;
		}
		ucRxCnt=0;
	}
}
